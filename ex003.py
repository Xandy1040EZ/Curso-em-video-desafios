# Soma entre dois Valores

a = int(input('Digite um número:  '))
b = int(input('Digite outro número: '))
# int é referente aos números inteiros exemplo: 1, 2, 3, ... (números sem virgula ou pontos exemplo 1.5)

s = int(a) + int(b)

print('Resultado da soma entre {0} e {1} é igual à {2} ' .format(a ,b ,s))  # A nùmeração entre as {} representa as posicões
