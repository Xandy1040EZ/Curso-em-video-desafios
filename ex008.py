M = float(input('Digite um valor em METROS (M): '))
print('Valor digitado foi de {}M '.format(M))
print('Esse valor de {} Metros em centimetros é igual a {} cm '
      .format(M, (100//M)) )
print('Esse valor {} Metros é igual à {} milimetros '.format(M,(1000//M)))







